    "            features.extend([inp_c / total, out_c / total])",
    "",
    "        # Component features",
    "        features.extend([",
    "            inp_f['num_components_4'] / 20.0,",
    "            inp_f['num_components_8'] / 20.0,",
    "            out_f['num_components_4'] / 20.0,",
