    "cosine similarity to find similar solved training tasks.",
    "'''",
    "",
    "def compute_task_embedding(task: Dict) -> np.ndarray:",
    '    """',
    "    Compute a feature vector from all training pairs of a task.",
    "    Features: shape ratios, color counts, component counts, density, symmetry.",
    "    Returns a normalized float vector.",
    "    '''",
    "    features = []",
    '    pairs = task.get(\'train\', [])',
    "    if not pairs:",
    "        return np.zeros(32, dtype=np.float32)",
    "",
    "    for pair in pairs:",
    "        inp_f = extract_grid_features(pair['input'])",
    "        out_f = extract_grid_features(pair['output'])",
    "",
    "        # Shape features",
    "        inp_h, inp_w = inp_f['height'], inp_f['width']",
    "        out_h, out_w = out_f['height'], out_f['width']",
    "        max_dim = max(inp_h, inp_w, out_h, out_w, 1)",
    "        features.extend([",
    "            inp_h / 30.0, inp_w / 30.0,",
    "            out_h / 30.0, out_w / 30.0,",
    "            (out_h - inp_h) / max_dim,",
