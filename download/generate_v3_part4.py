import json

def L(*lines):
    result = []
    for i, line in enumerate(lines):
        if i < len(lines) - 1:
            result.append(line + "\n")
        else:
            result.append(line)
    return result

def md_cell(*lines):
    return {"cell_type": "markdown", "metadata": {}, "source": L(*lines)}

def code_cell(*lines):
    return {"cell_type": "code", "execution_count": None, "metadata": {}, "outputs": [], "source": L(*lines)}

