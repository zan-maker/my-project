    "\n",
    "1. **Heuristics** (70+ solvers) \u2192 if 100% match, use it\n",
    "2. **Object-centric reasoning** \u2192 consistent transform detection\n",
    "3. **Similarity-based transfer** \u2192 use heuristics from similar training tasks\n",
    "4. **LLM self-consistency** \u2192 3 candidates at T=0.2, 0.4, 0.6\n",
    "5. **Evolutionary synthesis** \u2192 mutate/cross-over LLM candidates\n",
    "6. **Test-time LoRA training** \u2192 last resort PEFT fine-tuning\n",
